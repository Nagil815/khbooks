package dto;

public class AuthorDTO {
	private int auno;
	private String auname;
	private String auintro;
	
	public int getAuno() {
		return auno;
	}
	
	public void setAuno(int auno) {
		this.auno = auno;
	}
	
	public String getAuname() {
		return auname;
	}
	
	public void setAuname(String auname) {
		this.auname = auname;
	}
	
	public String getAuintro() {
		return auintro;
	}
	
	public void setAuintro(String auintro) {
		this.auintro = auintro;
	}
	
}
